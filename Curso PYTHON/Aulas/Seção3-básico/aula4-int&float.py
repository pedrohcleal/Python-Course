#tipos int e  float
#int -> número inteiro
#o tipo de int representa qualquer numero
#positivo ou negativo. int sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0)


#float -> numero com ponto flutuante
#o tipo float represeta qualquer número
#posito ou negativo com ponto flutuante.
#float sem sinal é considerado positivo.
print('"pedro"','=',type('pedro'))
print(1,'=',type(1))
print(1.1,'=',type(1.1))
print('"1"','=',type('1'))
print('"1.1"','=',type('1.1'))

#a função type mostra o tipo que o python interferiu o valor
